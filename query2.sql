SELECT 
(SUM(FAKTURY.BRUTTO) - (SUM(FAKTURY.BRUTTO)*CLIENTS_GROUP.RABAT/100)) as brutto,
SUM(FAKTURY.BRUTTO) as brutto_before,
CLIENTS_GROUP.RABAT,
MIASTA.SIZE as size, 
CLIENTS_GROUP.symbol
FROM FAKTURY
INNER JOIN table1 ON table1.id = FAKTURY.user_id
INNER JOIN MIASTA ON MIASTA.id = table1.city_id
INNER JOIN CLIENTS_GROUP ON CLIENTS_GROUP.id = table1.group_id
WHERE FAKTURY.MONTH = 'WRZESIEŃ'
GROUP BY size



